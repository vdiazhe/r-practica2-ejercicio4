#' suma
#' @description suma dos números
#' @param x Primer número
#' @param y Segundo número
#'
#' @return El resultado de sumar x más y
#' @export
suma <- function(x, y){
  x + y
}

#' resta
#' @description resta dos números
#' @param x Primer número
#' @param y Segundo número
#'
#' @return El resultado de restar x menos y
#' @export
resta <- function(x, y){
  x - y
}
 
#' multiplicacion
#' @description multiplica dos números
#' @param x Primer número
#' @param y Segundo número
#'
#' @return El resultado de multiplicar x por y
#' @export 
multiplicacion <- function(x, y){
  x * y
}

#' division
#' @description divide un número entre otro
#' @param x Dividendo
#' @param y Divisor
#'
#' @return El resultado de dividir x entre y
#' @export 
division <- function(x, y){
  x / y
}
